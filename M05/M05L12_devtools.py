### 🔴 HTML

# Strony internetowe to pod spodem m.in. KOD ŹRÓDŁOWY, czyli plik HTML. Jest to specjalny format danych (właściwie to język znaczników - markup language).

# Weźmy taki HTML:

html = '''
<p class="paragraph">This is a paragraph.</p>
'''

# W tym HTML'u mamy TAG p. 
# Tag P oznacza akapit (p=paragraph).
# Tag IMG oznacza obraz.
# Tag B oznacza wytłuszczony tekst (b=bold).
# Tag DIV oznacza kontener (grupę elementów).
# Tag H1 oznacza nagłówek pierwszego rzędu (H1=Header1).
# Tag A oznacza link.
# Jest wiele, wiele różnych tagów.

# Na początku jest tag OTWIERAJĄCY, a na końcu ZAMYKAJĄCY.

# Otwierając tag może zawierać ATRYBUTY - ich NAZWY (tutaj "class") oraz WARTOŚCI (tutaj "paragraph").

