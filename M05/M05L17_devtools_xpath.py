### 🔴 Automatyczne generowanie XPATH

# Ręczne konstruowanie ścieżek XPATH jest żmudne. Na szczęście wiodące przeglądarki potrafią same skonstruować taką ścieżkę.

# W tym celu wystarczy uruchomić narzędzia deweloperskie, kliknąć prawym przyciskiem na interesującym nas elemencie, wybrać "inspect", a następnie w kodzie HTML kliknąć prawym przyciskiem na interesującym nas tagu, wybrać "Copy" i "Copy XPATH".

### 🔴 Ćwiczenie

# Wejdź na stronę Leroy Merlin na dowolną stronę z listą produktów. Otwórz narzędzia deweloperskie. Skopiuj XPATH nazw produktów. Następnie zmodyfikuj ten XPATH tak, aby znajdował nazwy wszystkich produktów, a nie tylko jednego.