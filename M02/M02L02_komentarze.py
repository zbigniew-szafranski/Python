### 🔴 Praktyczne zastosowania 

# 👉 Użyj skrótu Ctrl+/ aby zakomentować i odkomentować kod. (Pamiętaj, aby na końcu usunąć kod-śmieci)
# 👉 Przydatne informacje, których nie da się zawrzeć w kodzie - co do zasady unikaj komentarzy i staraj się zawrzeć wszystkie informacje w kodzie, jeśli się tylko da.

# ❌ Bardzo źle:

a = 10
b = 20
c = a * b
print(c)

# ❌ Źle:

a = 10  # width
b = 20  # height
c = a * b  # area
print(c)

# ✅ Dobrze:

width = 10
height = 20
area = width * height
print(area)

